let fruits = ['maçã', 'banana', 'laranja', 'manga']

fruits.push('uva') //adiciona um item no final do array

console.log(fruits)

fruits.unshift('morango') //adiciona um item no inicio do array

console.log(fruits)

fruits.pop() //remove o ultimo item do array

console.log(fruits)

fruits.shift() //remove o primeiro item do array

console.log(fruits)

fruits.join('-') //junta os itens do array em uma string

console.log(fruits.join(', '))

